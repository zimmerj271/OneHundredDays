##CONFIGURE TABLES
class User(UserMixin, db.Model):
    __tablename__ = "users"
    id = db.Column(db.Integer, primary_key=True)
    name = db.Column(db.String(250), nullable=False)
    email = db.Column(db.String(250), nullable=False)
    password = db.Column(db.String(250), nullable=False)
    # act like a list of BlogPost objects attached to each user
    # author refers to the author property in BlogPost class
    # this will allow the author property of BlogPost to be a User object.
    posts = relationship("BlogPost", back_populates="author")
    comments = relationship("Comment", back_populates="commentor")


class BlogPost(db.Model):
    __tablename__ = "blog_posts"
    id = db.Column(db.Integer, primary_key=True)
    # assign foreign key from users table
    author_id = db.Column(db.Integer, db.ForeignKey("users.id"))
    # reference to User object, refers to posts property in Users class
    author = relationship("User", back_populates="posts")
    title = db.Column(db.String(250), unique=True, nullable=False)
    subtitle = db.Column(db.String(250), nullable=False)
    date = db.Column(db.String(250), nullable=False)
    body = db.Column(db.Text, nullable=False)
    img_url = db.Column(db.String(250), nullable=False)
    comments = relationship("Comment", back_populates="parent_post")


class Comment(db.Model):
    __tablename__ = "comments"
    id = db.Column(db.Integer, primary_key=True)
    # assign a foreign key from the users table
    commentor_id = db.Column(db.Integer, db.ForeignKey("users.id"))
    # reference the comment to the User object, refers to comments in the Users class
    commentor = relationship("User", back_populates="comments")
    post_id = db.Column(db.Integer, db.ForeignKey("blog_posts.id"))
    parent_post = relationship("BlogPost", back_populates="comments")
    text = db.Column(db.String(250), nullable=False)




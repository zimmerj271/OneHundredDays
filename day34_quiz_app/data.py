import requests
from question_model import Question

class Data:

    def __init__(self):
        self.parameters = {
            "amount": 10,
            "type": "boolean",
            "category": 9
        }
        self.api_url = f"https://opentdb.com/api.php?amount={self.parameters['amount']}&type={self.parameters['type']}"
        self.quiz_questions = self.get_questions()
        self.question_bank = []
        self.init_questions()

    def get_questions(self):
        api_response = requests.get(url=self.api_url)
        api_response.raise_for_status()
        data = api_response.json()
        return data['results']

    def init_questions(self):
        for question in self.quiz_questions:
            question_text = question["question"]
            question_answer = question["correct_answer"]
            new_question = Question(question_text, question_answer)
            self.question_bank.append(new_question)



